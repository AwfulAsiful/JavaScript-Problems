function subtractTwoNumbers(num1,num2)
{
    var result = num1-num2;
    return result;
}


var total = subtractTwoNumbers(100,5);
console.log("Subtraction ",total); 


function divisionOfTwoNumbers(num1,num2)

{
    var result = num1/num2;
    return result;

}


var total = divisionOfTwoNumbers(25,5);

console.log("Total after division = ",total);